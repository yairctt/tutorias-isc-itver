const ICONOS = {
  pdf: "fa-file-pdf-o",
  word: "fa-file-word-o",
  excel: "fa-file-excel-o",
  img: "fa-file-image-o",
};

function crearFileRow(doc) {
  const icono = ICONOS[doc.tipo] || "fa-file-o";
  const btnVer = doc.verBtn
    ? `<a href="${doc.pdf}" target="_blank" class="btn-ver">
             <i class="fa fa-eye"></i> Ver
           </a>`
    : "";

  return `
    <div class="doc-file-row">
      <div class="doc-file-info">
        <i class="fa ${icono}"></i>
        <div>
          <span class="doc-file-name">${doc.nombre}</span>
          <span class="doc-file-desc">${doc.desc}</span>
        </div>
      </div>
      <div class="doc-file-actions">
        ${btnVer}
        <a href="${doc.pdf}" download class="btn-descargar">
          <i class="fa fa-download"></i> Descargar
        </a>
      </div>
    </div>
  `;
}

function renderProgramas() {
  /* Período */
  document.querySelectorAll(".doc-periodo span")
    .forEach(el => el.textContent = `Periodo: ${PERIODO}`);

  /* Programas */
  const elT1 = document.getElementById("files-tutoria-1");
  if (elT1) elT1.innerHTML = PROGRAMA_T1.map(crearFileRow).join("");

  const elT2 = document.getElementById("files-tutoria-2");
  if (elT2) elT2.innerHTML = PROGRAMA_T2.map(crearFileRow).join("");

  /* Recursos */
  const elRT1 = document.getElementById("files-recursos-t1");
  if (elRT1) elRT1.innerHTML = RECURSOS_T1.map(crearFileRow).join("");

  const elRT2 = document.getElementById("files-recursos-t2");
  if (elRT2) elRT2.innerHTML = RECURSOS_T2.map(crearFileRow).join("");

  const elRExtra = document.getElementById("files-recursos-extra");
  if (elRExtra) elRExtra.innerHTML = RECURSOS_EXTRA.map(crearFileRow).join("");
}

document.addEventListener("DOMContentLoaded", renderProgramas);